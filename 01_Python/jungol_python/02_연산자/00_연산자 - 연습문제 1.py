num1 = int(input())
num2 = int(input())

print(f"{num1} + {num2} =", num1 + num2)
print(f"{num1} - {num2} =", num1 - num2)
print(f"{num1} * {num2} =", num1 * num2)
print(f"{num1} / {num2} =", num1 / num2)
print(f"{num1} // {num2} =", num1 // num2)
print(f"{num1} % {num2} =", num1 % num2)
print(f"{num1} ** {num2} =", num1 ** num2)